'''
    *
   * *
  * * *
 * * * *
* * * * *

Outer loop -> বাইরের লুপ -> লাইন যতগুলো ততবার রান হবে -> 5
Inner loop -> ভিতরের লুপ -> স্টার এবং স্পেস প্রিন্ট করা

i -> space
1 -> 4 -> 5 - 1
2 -> 3 -> 5 - 2
3 -> 2 -> 5 - 3
4 -> 1 -> 5 - 4
5 -> 0 -> 5 - 5

(5 - i) সংখ্যক স্পেস হবে
i সংখ্যক স্টার হবে
'''
i = 1
while i <= 5:
    j = 1
    while j <= 5 - i:
        print(end=' ')
        j+=1
    j = 1
    while j <= i:
        print('*', end=' ')
        j+=1

    print()
    i+=1